package javaBasics;

public class EncapsulationExmp {
	private int num; // declaration of variable
	private String name;
 
	public int getNum() { // getter method
		return num;
	}
 
	public void setNum(int num) { //setter method
		this.num = num;
	}
 
	public String getName() {
		return name;
	}
 
	public void setName(String name) {
		this.name = name;
	}
}




