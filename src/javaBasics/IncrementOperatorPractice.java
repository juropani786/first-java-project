package javaBasics;

public class IncrementOperatorPractice {
public static void main(String[]args) {
	int a =  40;
	int b = 10;
	b = ++a;
	System.out.println(a);
	System.out.println(b);
	System.out.println(a/b);
	
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(a+b);
}		
}

