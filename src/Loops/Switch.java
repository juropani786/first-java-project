package Loops;

public class Switch {
	public static void main(String[] args) {
		 
		String name = "Nur";
 
		switch (name) {
 
		case "Kishwor":
			System.out.println("1. Kishwor is printed");
			break;
		case "Nur":
			System.out.println("2. Nur is printed");
			break;
		case "Ram":
			System.out.println("3. Ram is printed");
			break;
		case "Hari":
			System.out.println("4. Hari is printed");
			break;
		default:
			System.out.println("5. Kishwor is printed");
		}
	}	
}