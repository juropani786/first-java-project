package Loops;

public class SwitchClass {
	public static void main(String[] args) {
		String name = "Madhuram";
		switch (name) {
		case "Kishwor":
			System.out.println("Kishwor is printed");
			break;
		case " Ram":
			System.out.println(" Ram is printd");

			break;
		case "Madhu":
			System.out.println("Madhu is printed");
			break;
		case "Hari":
			System.out.println(" Hari is printed");
			break;
		default:
			System.out.println("default value");

		}
	}

}
