package Loops;

public class Switch2 {
	public static void main(String[]args) {
		String name = "Tara";
		switch (name) {
		case "Indu":
			System.out.println("Indu is printed");
			break;
			
		case "Sushil":
			
			System.out.println(" Sushil is printed");
			break;
		case "Raju":
			
			System.out.println("Raju is printed");
			break;
		case "Nabuu":
			
			System.out.println("Nabuu is printed");
			break;
			default:
				System.out.println("Indu is printed");
		}
	}

}
